#addition(+) used to add two number:

a=10
b=20
c=a+b
print(c)

#subraction(-) subtract the second number from the first number:

a=100
b=200
result=a-b
print(result)

#multiplication(*) multiplies two number

a=4
b=4
c=4*4
print(c)

#division(/) divides the first number by the second number

a=16
b=4
c=a/b
print(c)


#floor division(//) divides the first number by second number and returns the floor value

a=15
b=4
c=a//b
print(c)

#modulo(%) returns the remainder:

a=256
b=16
c=a%b
print(c)

#exponention(**)

a=3
b=3
c=a**b
print(c)
 
