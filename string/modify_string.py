#upper case : modify a string using upper() method:
a="hello world"
print(a.upper())

#Lower case : modify string using lower() method:

b="PYTHON IS AWSOME"
print(b.lower())

#Remove Whitespace : the strip() method is used to remove white space from the beginnig or end:
c="   i am python developer   "
print(c.strip())

#Replace string : we can replace a string using replace() method:

d="java is easy to learn"
print(d.replace("java","python"))